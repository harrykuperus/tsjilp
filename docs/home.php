                    <h2>What Tsjilp does</h2>
                </div>

                <p>Tsjilp is not a chatbot. It is a communication layer on top of normal messaging.</p>
                <p>You write your own messages. You decide what gets sent. The assistant helps when it is useful.</p>

                <div class="bullet-grid">
                    <div class="bullet-item">Improve unclear messages before sending</div>
                    <div class="bullet-item">Help write replies when you don't know where to start</div>
                    <div class="bullet-item">Summarize long conversations</div>
                    <div class="bullet-item">Reduce misunderstandings before they happen</div>
                    <div class="bullet-item">Keep track of context and open threads</div>
                    <div class="bullet-item">Help you express yourself more clearly</div>
                </div>

                <div class="quote">Tsjilp is a communication assistant that helps humans understand each other better in text.</div>

            </section>

            <section class="section" id="when-to-use">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M12 2C6.48 2 2 6.48 2 12s4.48 10 10 10 10-4.48 10-10S17.52 2 12 2zm-2 14.5v-9l6 4.5-6 4.5z"/></svg>
                    </div>
                    <h2>When to use Tsjilp</h2>
                </div>

                <p>Tsjilp works best when conversations have weight — when context matters, when words matter, when misunderstandings are costly.</p>

                <div class="when-grid">
                    <div class="when-block yes">
                        <h3>✓ Good fit</h3>
                        <ul class="when-list">
                            <li>Conversations become messy or unclear</li>
                            <li>Context matters to the other person</li>
                            <li>Multiple people are involved</li>
                            <li>Communication is emotionally difficult</li>
                            <li>You are losing track of replies</li>
                            <li>Misunderstandings happen often</li>
                            <li>You need help expressing yourself</li>
                        </ul>
                    </div>
                    <div class="when-block not">
                        <h3>✗ Not a good fit</h3>
                        <ul class="when-list">
                            <li>Simple one-line chats</li>
                            <li>Memes and reaction messages</li>
                            <li>Project management</li>
                            <li>Kanban boards</li>
                            <li>CRM workflows</li>
                            <li>Replacing email or calendar systems</li>
                        </ul>
                    </div>
                </div>

            </section>

            <section class="section" id="what-it-is-not">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M12 1L3 5v6c0 5.55 3.84 10.74 9 12 5.16-1.26 9-6.45 9-12V5z"/></svg>
                    </div>
                    <h2>What Tsjilp is not</h2>
                </div>

                <p>Tsjilp is a communication tool. It is not designed to replace workflow systems, team management platforms, or enterprise dashboards. There are already good tools for those things.</p>

                <p>The goal is conversation clarity — helping humans understand each other in text. Nothing more.</p>

                <div class="card">
                    <p style="margin:0; font-size:16px; line-height:1.85; color:#374151;">
                        Not Slack. Not Teams. Not a CRM. Not a task manager. Not an enterprise platform.<br><br>
                        Tsjilp is assisted communication between people. The assistant helps quietly. You stay in control.
                    </p>
                </div>

            </section>

