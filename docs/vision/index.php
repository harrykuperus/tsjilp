                    Vision
                </div>
                <h1>Why Tsjilp exists.</h1>
                <p>This is not the product documentation. It is the thinking behind it — why the problem is real, what the assistant is trying to do, and where this could go.</p>
            </section>

            <section class="section" id="communication-problems">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M11 15h2v2h-2zm0-8h2v6h-2zm.99-5C6.47 2 2 6.48 2 12s4.47 10 9.99 10C17.52 22 22 17.52 22 12S17.52 2 11.99 2z"/></svg>
                    </div>
                    <h2>Communication problems</h2>
                </div>

                <p>Humans are not naturally good at text communication. In real life, we rely on tone, facial expressions, gestures, and immediate feedback to stay understood. Text removes all of those signals.</p>

                <p>The result is familiar to everyone:</p>

                <ul>
                    <li>Messages that mean something different than intended</li>
                    <li>Conversations that spiral into misunderstandings</li>
                    <li>Long threads where the original point gets buried</li>
                    <li>Decisions that were made but never tracked</li>
                    <li>Open questions that never got answered</li>
                    <li>Context that needs to be repeated every time someone new joins</li>
                </ul>

                <p>These are not new problems. But most messaging tools have done nothing to solve them. They move messages faster. They add more features. They make it easier to send more.</p>

                <p>Tsjilp tries to do the opposite: help people communicate more clearly with fewer misunderstandings, less repetition, and less noise.</p>

                <div class="quote">Better conversations, not more messages.</div>

            </section>

            <section class="section" id="assistant-concept">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M20 2H4a2 2 0 0 0-2 2v18l4-4h14a2 2 0 0 0 2-2V4a2 2 0 0 0-2-2z"/></svg>
                    </div>
                    <h2>Communication assistant concept</h2>
                </div>

                <p>The word "assistant" gets used for a lot of things. Chatbots, voice interfaces, automated workflows, search engines. Most of them are about giving answers to questions.</p>

                <p>Tsjilp uses the concept differently. The assistant is not there to replace conversation. It is there to support the humans who are having it.</p>

                <div class="concept-grid">
                    <div class="concept-tile">
                        <h3>What the assistant is</h3>
                        <p>A private helper that reads your conversations and helps you communicate more clearly, respond more thoughtfully, and keep track of context.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>What the assistant is not</h3>
                        <p>A chatbot. Not a decision maker. Not a manager. Not a supervisor. Not a replacement for human judgment or human conversation.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>Who benefits</h3>
                        <p>Anyone who communicates frequently in text — remote workers, entrepreneurs, people in complex relationships, anyone managing many conversations at once.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>The key principle</h3>
                        <p>The assistant helps privately. It never takes over. Nothing the assistant does becomes visible to others unless the user explicitly sends it.</p>
                    </div>
                </div>

                <div class="quote">Tsjilp is a communication assistant that helps humans understand each other better in text.</div>

            </section>

            <section class="section" id="secretary">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M12 2a5 5 0 1 0 0 10A5 5 0 0 0 12 2zm0 12c-5.33 0-8 2.67-8 4v2h16v-2c0-1.33-2.67-4-8-4z"/></svg>
                    </div>
                    <h2>Assistant as secretary</h2>
                </div>

                <p>The most useful metaphor for the assistant is a quiet, competent secretary for communication.</p>

                <p>A good secretary does not make decisions for you. They prepare what you need, keep track of what matters, remind you of open items, and help you communicate more professionally and clearly. But they stay in the background. They do not speak on your behalf unless you ask them to.</p>

                <p>Tsjilp's assistant works the same way:</p>

                <ul>
                    <li>It reads what is in the conversation</li>
                    <li>It prepares suggestions privately</li>
                    <li>It tracks what needs your attention</li>
                    <li>It helps you express yourself more clearly</li>
                    <li>It stays invisible to others unless you choose to share something</li>
                </ul>

                <p>Another useful analogy is the e-bike. When you ride an e-bike, you still pedal. You still control the direction. The motor only assists when the terrain gets difficult. You remain the one riding.</p>

                <p>Tsjilp works like that. You still communicate. The assistant only helps where the conversation gets difficult — confusing messages, charged emotions, complicated replies.</p>

                <div class="card">
                    <p>The goal is not AI that talks for you. The goal is AI that helps you talk better.</p>
                </div>

            </section>

            <section class="section" id="chat-first">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M20 2H4a2 2 0 0 0-2 2v18l4-4h14a2 2 0 0 0 2-2V4a2 2 0 0 0-2-2z"/></svg>
                    </div>
                    <h2>Chat first philosophy</h2>
                </div>

                <div class="quote">Chat first, assistant second.</div>

                <p>This is the core idea behind all decisions in Tsjilp. The conversation is always the primary interface. The assistant is secondary to it.</p>

                <p>Tsjilp should always feel like a chat app where the assistant helps quietly — not an AI tool where humans are secondary participants. The moment the assistant starts dominating the experience, something has gone wrong.</p>

                <p>What this means in practice:</p>

                <ul>
                    <li>The chat is always visible and always primary</li>
                    <li>The assistant appears only when the user opens it</li>
                    <li>Nothing AI-generated reaches the conversation automatically</li>
                    <li>The user decides what to send and when to send it</li>
                    <li>AI suggestions are always framed as options, never as instructions</li>
                </ul>

                <p>Conversations are fluid. People join and leave. Context shifts. Misunderstandings happen. Tsjilp supports this natural flow instead of trying to force rigid structures on top of it.</p>

                <p>Advanced features appear only when the user interacts with them. The default view is a clean conversation. Everything else is one layer deeper.</p>

                <div class="card">
                    <p>If a feature cannot fit naturally around a conversation, it probably does not belong in Tsjilp.</p>
                </div>

            </section>

            <section class="section" id="design-philosophy">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M12 1L3 5v6c0 5.55 3.84 10.74 9 12 5.16-1.26 9-6.45 9-12V5z"/></svg>
                    </div>
                    <h2>Design philosophy</h2>
                </div>

                <p>These are the principles that keep Tsjilp from slowly becoming another overloaded tool. They are constraints as much as they are values.</p>

                <div class="rule-list">

                    <div class="rule">
                        <div class="rule-num">1</div>
                        <div class="rule-body">
                            <div class="rule-title">Chat is the center of everything</div>
                            <div class="rule-desc">The conversation must always remain the primary interface. Tsjilp is not a dashboard, workspace, or management tool. Everything else exists to support the chat, not replace it.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">2</div>
                        <div class="rule-body">
                            <div class="rule-title">The assistant never dominates</div>
                            <div class="rule-desc">The assistant behaves like a secretary — it suggests, clarifies, summarizes, and assists. It never interrupts, controls, or speaks for the user without being asked.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">3</div>
                        <div class="rule-body">
                            <div class="rule-title">Simplicity over features</div>
                            <div class="rule-desc">Every feature must pass one test: does this reduce communication friction? If not, it should not be added. Tsjilp is not a task manager, CRM, or collaboration suite.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">4</div>
                        <div class="rule-body">
                            <div class="rule-title">Progressive reveal</div>
                            <div class="rule-desc">Advanced features must remain hidden until needed. The default view is just chat, assistant, and input. Everything else appears only when the user interacts. This keeps the interface calm and readable.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">5</div>
                        <div class="rule-body">
                            <div class="rule-title">Conversations are fluid</div>
                            <div class="rule-desc">People do not communicate in rigid groups. Real conversations evolve — someone joins, someone leaves, someone contributes briefly. Tsjilp allows participants to enter when needed instead of forcing permanent group structures.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">6</div>
                        <div class="rule-body">
                            <div class="rule-title">Context should feel invisible</div>
                            <div class="rule-desc">Users should not feel like they are managing projects or channels. The assistant quietly recognizes connections and provides helpful context when relevant — without requiring the user to structure anything manually.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">7</div>
                        <div class="rule-body">
                            <div class="rule-title">Reduce cognitive load</div>
                            <div class="rule-desc">One of the biggest goals of Tsjilp is reducing mental overhead. The system should help users answer: who needs a reply from me? What is the next step? What are we waiting for?</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">8</div>
                        <div class="rule-body">
                            <div class="rule-title">Avoid communication noise</div>
                            <div class="rule-desc">Many tools create more noise than clarity. Tsjilp does the opposite. Silence and clarity are design goals. Simplify conversations, highlight important moments, hide unnecessary complexity.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">9</div>
                        <div class="rule-body">
                            <div class="rule-title">Support human communication behavior</div>
                            <div class="rule-desc">Humans misunderstand messages, react emotionally, forget context, and communicate differently depending on mood and relationship. Tsjilp supports this reality rather than trying to enforce strict communication rules.</div>
                        </div>
                    </div>

                    <div class="rule">
                        <div class="rule-num">10</div>
                        <div class="rule-body">
                            <div class="rule-title">Build for humans, not systems</div>
                            <div class="rule-desc">Many modern tools are built around data structures rather than human behavior. Tsjilp always prioritizes clarity, calm interfaces, and natural conversation flow. Technology exists to support human interaction, not replace it.</div>
                        </div>
                    </div>

                </div>

            </section>

            <section class="section" id="future-ideas">
                <div class="section-heading">
                    <div class="section-icon">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M12 4.5C7 4.5 2.73 7.61 1 12c1.73 4.39 6 7.5 11 7.5s9.27-3.11 11-7.5c-1.73-4.39-6-7.5-11-7.5zM12 17c-2.76 0-5-2.24-5-5s2.24-5 5-5 5 2.24 5 5-2.24 5-5 5zm0-8a3 3 0 1 0 0 6 3 3 0 0 0 0-6z"/></svg>
                    </div>
                    <h2>Future ideas</h2>
                </div>

                <p>These are ideas in the longer-term direction of Tsjilp. Not roadmap items. Not promises. Just the direction the thinking points toward.</p>

                <div class="concept-grid">
                    <div class="concept-tile">
                        <h3>AI contact memory</h3>
                        <p>The assistant builds a private picture of each contact over time — their communication style, what they care about, how they express frustration or enthusiasm — so every conversation gets more relevant help.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>Context engine</h3>
                        <p>Instead of reading only recent messages, the assistant connects context across conversations — recognizing when a topic in one chat is related to something said in another.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>Your communication voice</h3>
                        <p>Over time, the assistant learns how you communicate — your vocabulary, your rhythm, your preferred tone in different types of conversations. Suggestions start sounding unmistakably like you.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>Conversation transparency</h3>
                        <p>Every message can optionally show its origin: written by the user, improved by assistant, suggested by assistant. Nothing hides what it is.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>Voice input</h3>
                        <p>Speak your reply and let the assistant clean it up into written form in your voice. Useful when typing is inconvenient and speed matters.</p>
                    </div>
                    <div class="concept-tile">
                        <h3>External integrations</h3>
                        <p>Bring in messages from other platforms — email, WhatsApp, Telegram — so the assistant can help with all communication in one place without replacing those channels.</p>
                    </div>
                </div>

                <div class="quote">Tsjilp.me aims to become a communication layer where conversations remain simple while an assistant quietly helps people understand each other, respond thoughtfully, and stay organized.</div>

                <p style="margin-top:20px;">The goal is not to replace messaging tools. The goal is to make human communication in text work better.</p>

