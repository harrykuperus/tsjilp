                <h1>
                    A chat app with an intelligence layer.
                </h1>

                <p>
                    Tsjilp helps people communicate better in text.
                    The assistant helps you write, understand, and track conversations —
                    while you remain the one in control of every message.
                </p>

            </section>

            <section class="section" id="what-is-tsjilp">
                <div class="section-heading">
                    <div class="section-icon"><svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M12 2a10 10 0 1 0 10 10A10 10 0 0 0 12 2zm1 15h-2v-6h2zm0-8h-2V7h2z"/></svg></div>
                    <h2>What is Tsjilp</h2>
                </div>
                <p>Tsjilp is not a chatbot platform. It is a communication layer on top of normal messaging.</p>
                <p>You still write messages yourself. You still decide what gets sent. The assistant only helps when useful.</p>
                <div class="quote">Tsjilp should feel like a quiet assistant helping humans communicate better.</div>
            </section>

            <section class="section" id="why-tsjilp-exists">
                <div class="section-heading">
                    <div class="section-icon"><svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M11 15h2v2h-2zm0-8h2v6h-2zm.99-5C6.47 2 2 6.48 2 12s4.47 10 9.99 10C17.52 22 22 17.52 22 12S17.52 2 11.99 2z"/></svg></div>
                    <h2>Why Tsjilp exists</h2>
                </div>
                <p>Text removes all the signals that help humans understand each other in real life — tone, expressions, timing. The result is misunderstandings, lost context, and conversations that go in the wrong direction.</p>
                <p>Modern messaging tools move messages faster but do little to prevent misunderstandings. Tsjilp tries to do the opposite: help people communicate more clearly with less noise.</p>
                <div class="steps">
                    <div class="step">
                        <div class="step-num">1</div>
                        <div class="step-body">
                            <div class="step-title">Misunderstandings happen in text</div>
                            <div class="step-desc">Without tone and expressions, messages are frequently misread. Short replies seem cold. Sarcasm lands wrong. Ambiguous phrasing causes friction.</div>
                        </div>
                    </div>
                    <div class="step">
                        <div class="step-num">2</div>
                        <div class="step-body">
                            <div class="step-title">Context gets lost across conversations</div>
                            <div class="step-desc">Important context ends up scattered across chats. Catching up is difficult. New participants miss background that everyone else already knows.</div>
                        </div>
                    </div>
                    <div class="step">
                        <div class="step-num">3</div>
                        <div class="step-body">
                            <div class="step-title">Cognitive load adds up</div>
                            <div class="step-desc">Managing many text conversations simultaneously is mentally exhausting. It is hard to know what needs a reply, what can wait, and what was already handled.</div>
                        </div>
                    </div>
                    <div class="step">
                        <div class="step-num">4</div>
                        <div class="step-body">
                            <div class="step-title">Tsjilp helps with all of these</div>
                            <div class="step-desc">The assistant helps you write more clearly, understand what others mean, keep track of what matters, and reduce the mental overhead of managing conversations.</div>
                        </div>
                    </div>
                </div>
            </section>

            <section class="section" id="who-it-is-for">
                <div class="section-heading">
                    <div class="section-icon"><svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor"><path d="M16 11c1.66 0 2.99-1.34 2.99-3S17.66 5 16 5c-1.66 0-3 1.34-3 3s1.34 3 3 3zm-8 0c1.66 0 2.99-1.34 2.99-3S9.66 5 8 5C6.34 5 5 6.34 5 8s1.34 3 3 3zm0 2c-2.33 0-7 1.17-7 3.5V19h14v-2.5c0-2.33-4.67-3.5-7-3.5zm8 0c-.29 0-.62.02-.97.05 1.16.84 1.97 1.97 1.97 3.45V19h6v-2.5c0-2.33-4.67-3.5-7-3.5z"/></svg></div>
                    <h2>Who it is for</h2>
                </div>
                <p>Tsjilp is for people who communicate frequently in text and who recognise any of these situations:</p>
                <div class="steps">
                    <div class="step">
                        <div class="step-num">1</div>
