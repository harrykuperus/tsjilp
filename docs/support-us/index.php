function setFrequency(period, btn) {
    selectedFrequency = period;
    document.querySelectorAll('.toggle-btn').forEach(function (b) { b.classList.remove('active'); });
    btn.classList.add('active');
    resetCheckout();
}

function selectAmount(amount, btn) {
    selectedAmount = amount;
    document.querySelectorAll('.amount-btn').forEach(function (b) { b.classList.remove('selected'); });
    btn.classList.add('selected');
    var customWrap = document.getElementById('custom-amount-wrap');
    if (amount === 'custom') {
        customWrap.classList.add('visible');
        document.getElementById('custom-amount').focus();
    } else {
        customWrap.classList.remove('visible');
    }
    resetCheckout();
}

function resetCheckout() {
    if (stripeCheckout) {
        stripeCheckout.destroy();
        stripeCheckout = null;
    }
    var wrap = document.getElementById('checkout-wrap');
    if (wrap) wrap.classList.remove('visible');
    var container = document.getElementById('checkout-container');
    if (container) container.innerHTML = '';
}

function handleContinue() {
    var amount = selectedAmount;
    if (amount === 'custom') {
        amount = parseFloat(document.getElementById('custom-amount').value);
        if (!amount || amount < 1) {
            document.getElementById('custom-amount').focus();
            return;
        }
    }
    var amountCents = Math.round(amount * 100);
    var email      = document.getElementById('support-email').value.trim();
    var frequency  = selectedFrequency;

    var btn = document.getElementById('donate-btn');
    btn.disabled = true;
    btn.textContent = 'Loading…';

    resetCheckout();

    fetch('create-checkout-session.php', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ amount_cents: amountCents, frequency: frequency, email: email })
    })
    .then(function (res) { return res.json(); })
    .then(function (data) {
        btn.disabled = false;
        btn.textContent = 'Donate';

        if (data.error) {
            alert('Could not start checkout: ' + data.error);
            return;
        }

        if (!stripeInstance) {
            stripeInstance = Stripe(STRIPE_PUBLISHABLE_KEY);
        }

        return stripeInstance.initEmbeddedCheckout({ clientSecret: data.clientSecret })
            .then(function (checkout) {
                stripeCheckout = checkout;
                var wrap = document.getElementById('checkout-wrap');
                wrap.classList.add('visible');
                checkout.mount('#checkout-container');
                wrap.scrollIntoView({ behavior: 'smooth', block: 'start' });
            });
    })
    .catch(function (err) {
        btn.disabled = false;
        btn.textContent = 'Donate';
        alert('Something went wrong. Please try again.');
        console.error('[stripe]', err);
    });
}

function toggleFaq(btn) {
    var item = btn.closest('.faq-item');
    item.classList.toggle('open');
}
